class Navigator {

	constructor(levels) {

		this.level = 0;
		this.X = 0;
		this.Y = 0;

		this.levels = levels;
		this.limit = levels.length - 1;
		this.size = levels[0].length;

	}

	Avanzar(addX, addY) {
		this.X = this.X + addX;
		this.Y = this.Y + addY;

		let val = this.levels[this.Y][this.X % this.size];
		return val;
	}

	Run(addX, addY) {
		let c = 0;
		while (this.Y < this.limit) {
			let val = this.Avanzar(addX, addY);
			if (val === "#") c++;
		}
		this.X = 0;
		this.Y = 0;
		return c;
	}

}

